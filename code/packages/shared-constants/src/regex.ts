/**
 * 正则表达式常量
 */

export const REGEX = {
  /** 手机号 */
  MOBILE: /^1[3-9]\d{9}$/,
  
  /** 邮箱 */
  EMAIL: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
  
  /** 身份证号 */
  ID_CARD: /(^\d{15}$)|(^\d{18}$)|(^\d{17}(\d|X|x)$)/,
  
  /** 中文 */
  CHINESE: /^[\u4e00-\u9fa5]+$/,
  
  /** 字母和数字 */
  ALPHANUMERIC: /^[a-zA-Z0-9]+$/,
  
  /** 纯数字 */
  NUMBER: /^\d+$/,
  
  /** IP地址 */
  IP: /^(\d{1,3}\.){3}\d{1,3}$/,
  
  /** URL */
  URL: /^https?:\/\/.+/,
  
  /** 强密码(至少8位,包含大小写字母、数字和特殊字符) */
  STRONG_PASSWORD: /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/
} as const;

